
public class ElFor{
	/*
		Formato del for:
			Parte 1: Definir Contador y Darle un valor inicial
			parte 2: La condicion que se tiene que cumplir 
			parte 3: Modificacion del contador

			for(parte 1; parte 2; parte 3){
			//instrucciones a repetir
			}
	*/
		public static void main(String[] args){
			int n = 10;

			for(int co=1; co <= n; co++){
				System.out.println(co);
			}
		}

	}
